package com.cmsv1.bean.properties;

public class ReportElementProp
{
    public String preId;
    public String preLabel;
    public String preElement;
    public String preExt;
    public String preData;

    public String getPreId()
    {
        return preId;
    }

    public void setPreId(String preId)
    {
        this.preId = preId;
    }

    public String getPreLabel()
    {
        return preLabel;
    }

    public void setPreLabel(String preLabel)
    {
        this.preLabel = preLabel;
    }

    public String getPreElement()
    {
        return preElement;
    }

    public void setPreElement(String preElement)
    {
        this.preElement = preElement;
    }

    public String getPreExt()
    {
        return preExt;
    }

    public void setPreExt(String preExt)
    {
        this.preExt = preExt;
    }

    public String getPreData()
    {
        return preData;
    }

    public void setPreData(String preData)
    {
        this.preData = preData;
    }
}
